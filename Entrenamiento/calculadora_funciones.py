    #VALIDANDO EL DENOMINADOR (debe ser diferente de cero)
    if numero_dos!=0:
      resultado=calcular_division(numero_uno,numero_dos)
      print("Resultado=",resultado)
    else:
      print("¡No es posible dividir entre cero!")

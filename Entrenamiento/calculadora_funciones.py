    print("Vamos a calcular la raíz cuadrada de los valores ingresados:\n")
    resultado1 = calcular_radicacion(numero_uno)
    resultado2 = calcular_radicacion(numero_dos)
    print(f"La raíz cuadrada de {numero_uno} es: {resultado1}")
